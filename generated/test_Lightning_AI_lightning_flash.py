    batch[0][1]['img_scale'] = torch.ones_like(images[:, 0, 0, 0]).unsqueeze(1)
    batch[0][1]['img_size'] = (torch.ones_like(images[:, 0, 0, 0]) * images[0].shape[-1]).unsqueeze(1).repeat(1, 2)
